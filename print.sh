# Printing in shell is done by echo command
# syntax : echo INPUT MESSAGE

echo world
echo Hello World

# We can also print text in colours
# Syntax : echo -e "\e[COmMESSAGE\e[0m"

## -e - To enable \e
## \e[ - To enable colours
## COL - colour code
## m - End of Syntax
## 0 - To disable colour

echo hello World
